public class Media {
    public static void main(String[] args) {
        float mediaA = (8 + 9 + 7) / 3;
        float mediaB = (4 + 5 + 6) / 3;

        float mediaTotal = (mediaA + mediaB) / 2;

        if (mediaTotal < 7) {
            System.out.println("Aluno Reprovado");

        } else {
            System.out.println("Aluno Aprovado");
        }

    }

}
